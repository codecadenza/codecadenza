package net.codecadenza.runtime.avro.domain;

import java.io.Serializable;

/**
 * <p>
 * Domain object for testing references
 * </p>
 * <p>
 * Copyright 2025 (C) by Martin Ganserer
 * </p>
 * @author Martin Ganserer
 * @version 1.0.0
 */
public class ReferenceObject implements Serializable {
	private static final long serialVersionUID = 3446923079231404165L;

	private long id;
	private String name;

	/**
	 * @return the id
	 */
	public long getId() {
		return this.id;
	}

	/**
	 * @param id the id to set
	 */
	public void setId(long id) {
		this.id = id;
	}

	/**
	 * @return the name
	 */
	public String getName() {
		return this.name;
	}

	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

}