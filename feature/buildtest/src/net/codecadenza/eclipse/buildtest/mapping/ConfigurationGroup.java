package net.codecadenza.eclipse.buildtest.mapping;

import jakarta.xml.bind.annotation.XmlAccessType;
import jakarta.xml.bind.annotation.XmlAccessorType;
import jakarta.xml.bind.annotation.XmlAttribute;
import jakarta.xml.bind.annotation.XmlElement;
import java.io.Serializable;
import java.util.ArrayList;
import java.util.List;

/**
 * <p>
 * JAXB mapping class for configuration groups
 * </p>
 * <p>
 * Copyright 2025 (C) by Martin Ganserer
 * </p>
 * @author Martin Ganserer
 * @version 1.0.0
 */
@XmlAccessorType(XmlAccessType.FIELD)
public class ConfigurationGroup implements Serializable {
	private static final long serialVersionUID = 7712687201063238572L;

	@XmlAttribute(required = true)
	private String name;

	@XmlElement(required = false)
	private String description;

	@XmlElement(name = "config", required = false)
	private List<ProjectConfiguration> configurations = new ArrayList<>();

	/**
	 * @return the name
	 */
	public String getName() {
		return this.name;
	}

	/**
	 * @param name
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * @return the description
	 */
	public String getDescription() {
		return this.description;
	}

	/**
	 * @param description
	 */
	public void setDescription(String description) {
		this.description = description;
	}

	/**
	 * @return a list of project configuration elements
	 */
	public List<ProjectConfiguration> getConfigurations() {
		return this.configurations;
	}

	/**
	 * @param configurations
	 */
	public void setConfigurations(List<ProjectConfiguration> configurations) {
		this.configurations = configurations;
	}

}
